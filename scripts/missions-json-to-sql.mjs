/**
 * data/global_mission.json → Supabase용 INSERT SQL 생성
 * 사용: node scripts/missions-json-to-sql.mjs
 * 결과: scripts/generated-missions-insert.sql
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');

function sqlLiteral(s) {
  return "'" + String(s ?? '').replace(/'/g, "''") + "'";
}

/** YYYY-MM-DD + 1일 (UTC 달력) */
function nextDay(dayStr) {
  const [y, mo, d] = dayStr.split('-').map(Number);
  const dt = new Date(Date.UTC(y, mo - 1, d));
  dt.setUTCDate(dt.getUTCDate() + 1);
  const y2 = dt.getUTCFullYear();
  const m2 = String(dt.getUTCMonth() + 1).padStart(2, '0');
  const d2 = String(dt.getUTCDate()).padStart(2, '0');
  return `${y2}-${m2}-${d2}`;
}

const jsonPath = path.join(root, 'data', 'global_mission.json');
const outPath = path.join(root, 'scripts', 'generated-missions-insert.sql');

const raw = fs.readFileSync(jsonPath, 'utf8');
const data = JSON.parse(raw);
if (!Array.isArray(data.missions)) {
  throw new Error('JSON에 missions 배열이 없습니다.');
}

const chunks = [];
chunks.push(
  '-- generated by scripts/missions-json-to-sql.mjs from data/global_mission.json',
  '-- valid_from = day 12:00 UTC, valid_to = 다음날 12:00 UTC',
  '-- 이미 같은 mission_date에 published 가 있으면 유니크 충돌 → 해당 날짜 정리 후 실행',
  '',
  'begin;',
  '',
);

for (const m of data.missions) {
  const day = m.day;
  const nd = nextDay(day);
  const vf = `${day} 12:00:00+00`;
  const vt = `${nd} 12:00:00+00`;
  chunks.push(`insert into public.missions (
  mission_date,
  category_key,
  title,
  short_description,
  notice_text,
  is_time_sensitive,
  status,
  valid_from,
  valid_to
) values (
  ${sqlLiteral(day)}::date,
  ${sqlLiteral(m.category_key)},
  ${sqlLiteral(m.title)},
  ${sqlLiteral(m.short_description)},
  ''::text,
  false,
  'published',
  ${sqlLiteral(vf)}::timestamptz,
  ${sqlLiteral(vt)}::timestamptz
);`);
  chunks.push('');
}

chunks.push('commit;', '');

fs.writeFileSync(outPath, chunks.join('\n'), 'utf8');
console.log('wrote', outPath, '(' + data.missions.length + ' inserts)');
